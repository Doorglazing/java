public class demo2 {
    public static void main(String[] args) {
        int a = 567;
        int b = 234;
        /*
        * if(条件表达式){
        * 条件成立执行的语句
        * }else{
        * 条件不成立时执行的语句
        * }
        * */
        if(a > b){
            System.out.println(a);
            return;
        }
        System.out.println(b);
    }
}
